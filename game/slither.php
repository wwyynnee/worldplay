                  <p>Основная задача в игре Slither.io - стать самым большим червем. Для наращивания массы нужно собирать цветные точки, которые рассеяны по всей карте. Кроме того, погибший черв распадается на такие же светящиеся точки. Так что второй способ добычи массы — помогать соперникам погибнуть и затем поглотить их массу.<br/>Червь в Slither.io погибает, когда ударяет свою голову. Даже самый огромный червь погибнет, стукнув голову о самого маленького. Выживание зависит от умения маневрировать и ни в кого не врезаться.<br/>Единственное умение, которым располагают игроки в Slither.io - ускорение. Зажав клик мыши, можно ускориться, но за каждую секунду ускорения червь теряет массу. А еще возникают сложности с маневрированием, что становится очень опасным, когда вокруг много других игроков.<br/>Одновременно на карте примерно 500 игроков. Это значит, что в каком районе игры Слизарио вы ни оказались, там есть с кем по сражаться. Обычно много игроков в центре, а потом, став гигантскими и требуя больше простора, черви расходятся к краям карты. Важно понимать, что в красный край карты врезаться нельзя.</p>
